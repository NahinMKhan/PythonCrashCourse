friends = ['Nabeeh', 'Eugene', 'Chris', 'David', 'Maruf', 'Adyan', 'Ruhan', 'Daniel']
print('The first three items in the list are:')
print(friends[:3]) #prints the first three items

print('\n The three items in the middle of the list are')
print(friends[3:6]) #prints items from index 3 to index 6

print('\n The last three items in the list are:')
print(friends[-3:]) #prints the last three items
